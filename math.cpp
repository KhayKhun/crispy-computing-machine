#include <iostream>
#include <cmath>
using namespace std;

int main(){
    cout << max(2,9) << endl;
    cout << hypot(3,4) << endl;
    cout << min(2,9) << endl;
    cout << pow(2,3) << endl; // 2^3
    cout << sqrt(6) << endl;
    cout << round(2.5) << endl;
    cout << round(2.4) << endl;
    cout << ceil(2.5) << endl;
    cout << floor(2.5) << endl;

    return 0;
}